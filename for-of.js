let myArray = [2,4,6,8]

/**
 * for of percorre pelos valores de objetos que sejam iteratvos,
 * como por exemplo um Array, map, set, etc
 */
for (let value of myArray) {
  console.log(value)
}

let veiculo = 'Bicicleta'
/**
 * conseguimos ter cada letra da string separadas, 
 * usando o for of.
 */
for (let letter of veiculo) {
  console.log(letter)
}